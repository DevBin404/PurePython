print("Enter Atomic Number:")
x = input()
Proton_Number = int(x)
if Proton_Number == 1:
    print("Hydrogen")
elif Proton_Number == 2:
    print("Helium")
elif Proton_Number == 3:
    print("Lithium")
elif Proton_Number == 4:
    print("Beryllium")
elif Proton_Number == 5:
    print("Boron")
elif Proton_Number == 6:
    print("Carbon")
elif Proton_Number == 7:
    print("Nitrogen")
elif Proton_Number == 8:
    print("Oxygen")
elif Proton_Number == 9:
    print("Fluorine")
elif Proton_Number == 10:
    print("Neon")
else :
    print("Not Yet")    